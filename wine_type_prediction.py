# splitting the data into target variable and features
X = data.drop("type", axis=1)
y = data["type"]

# multivariate analysis - heatmap
import seaborn as sns
import matplotlib.pyplot as plt
plt.figure(figsize=(10,10))
sns.heatmap(X.corr(), annot=True)
plt.show()

# train test split
from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X,y, test_size=0.2, stratify=y,random_state=42)

# logistic regressor
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import classification_report, confusion_matrix

lr = LogisticRegression()
lr.fit(X_train, y_train)

y_pred_lr = lr.predict(X_test)

print(classification_report(y_test, y_pred_lr))
cm_lr = confusion_matrix(y_test, y_pred_lr)
sns.heatmap(cm_lr,fmt='d', annot=True)
plt.show()

# decision tree classifier
from sklearn.tree import DecisionTreeClassifier
# random forest classifier
from sklearn.ensemble import RandomForestClassifier

dtc = DecisionTreeClassifier()
dtc.fit(X_train, y_train)

y_pred_dtc = dtc.predict(X_test)

print(classification_report(y_test, y_pred_dtc))
cm_dtc = confusion_matrix(y_test, y_pred_dtc)
sns.heatmap(cm_dtc,fmt='d', annot=True)
plt.show()

rfc = RandomForestClassifier()
rfc.fit(X_train, y_train)

y_pred_rfc = rfc.predict(X_test)

print(classification_report(y_test, y_pred_rfc))
cm_rfc = confusion_matrix(y_test, y_pred_rfc)
sns.heatmap(cm_rfc,fmt='d', annot=True)
plt.show()

# download the dtc model using joblib
import joblib
joblib.dump(dtc, "dtc_model.pkl")

